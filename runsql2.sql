-- CREATE TABLE statements
CREATE TABLE HR.DEPARTMENTS (
  ID NUMBER PRIMARY KEY,
  NAME VARCHAR2(100)
);

CREATE TABLE HR.JOBS (
  ID NUMBER PRIMARY KEY,
  TITLE VARCHAR2(100)
);

CREATE TABLE HR.LOCATIONS (
  ID NUMBER PRIMARY KEY,
  CITY VARCHAR2(100),
  COUNTRY VARCHAR2(100)
);

CREATE TABLE HR.EMPLOYEES (
  ID NUMBER PRIMARY KEY,
  NAME VARCHAR2(100),
  SALARY NUMBER,
  DEPARTMENT_ID NUMBER REFERENCES HR.DEPARTMENTS(ID),
  JOB_ID NUMBER REFERENCES HR.JOBS(ID),
  LOCATION_ID NUMBER REFERENCES HR.LOCATIONS(ID)
);

CREATE TABLE HR.JOB_HISTORY (
  ID NUMBER PRIMARY KEY,
  EMPLOYEE_ID NUMBER REFERENCES HR.EMPLOYEES(ID),
  JOB_ID NUMBER REFERENCES HR.JOBS(ID),
  START_DATE DATE,
  END_DATE DATE
);

